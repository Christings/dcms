package com.web.bean.result;

/**
 * 域自定义返回
 *
 * @author 杜延雷
 * @date 2016/10/26.
 */
public class DoMainResult {
	/**
	 * id
	 */
	private String id;

	/**
	 * 域名称
	 */
	private String name;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
