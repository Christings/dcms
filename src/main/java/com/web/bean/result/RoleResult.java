package com.web.bean.result;

/**
 * 角色自定义返回
 *
 * @author 杜延雷
 * @date 2016/10/26.
 */
public class RoleResult {
	/**
	 * id
	 */
	private String id;
	/**
	 * 角色名
	 */
	private String rolename;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getRolename() {
		return rolename;
	}

	public void setRolename(String rolename) {
		this.rolename = rolename;
	}
}
